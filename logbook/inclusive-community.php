        <div class="d-flex _content">
            <div class="col col-12 col-md-6 offset-md-2">
                <p><strong>What does an inclusive community mean to us?</strong></p>
                <p>It is a community that respects its residents, gives them full access to as many possible resources, promotes equal treatment and opportunities, and values diversity.</p>
                <p>Are you looking for a space that fosters relationships between people from two extreme parts of India? Are you looking for a space that allows you to absolutely own your sexual orientation? Finally, are you also looking for a space that allows couples to live together, whether married or unmarried? Look no further, Boston Living allows for all this. It gives you an opportunity to foster friendships with people from all over the country, and the kind of friendships that last a lifetime.</p>
                <p>We are completely against differentiation based on caste, creed, colour or sexual orientation. We breathe and believe that inclusive communities are stronger communities. The greater the diversity of people in our community, the greater is its richness in culture owing to people coming together from different parts of the country. A country that is known for its unity in diversity.</p>
                <p>Steps we take to make the community a better place:</p>
                <ul>
                    <li>1. <strong>Creating Spaces for Conversation:</strong> We have an abundant common area, where we have provided seating for residents to catch up, have meaningful conversations and get to know each other. The team that handles the property is also very open to conversations, be it personal or professional. The team is very happy to lend a ear. We love to care for all our residents.</li>
                    <li>2. <strong>Providing spaces to play and bond:</strong> All work and no play, makes a Bostonite a dull person. This is derived from the famous proverb. However, how we like to say it is, work hard and play harder. In order to achieve this we have facilities that include a fully equipped fitness suite, dance studio, games room with games like snooker, XBox, PS 4, carroms, etc. where the residents can chill and compete with each other. What’s a friendship without some competitions right?</li>
                    <li>3. <strong>Celebrate the Community:</strong> India is a land of multiple religions and multiple faiths means a million festivals to celebrate. We go big and celebrate every festival, and ensure all the residents have fun. Activities range from pani puri competitions to decor to fun and games. People go back with tons of memories and new bonds everytime. It is endearing to see so much love and oneness amongst all the residents. We also believe in celebrating small occasions and surprising the community. You never know, you might just come home to a community cake cutting.</li>
                </ul>
