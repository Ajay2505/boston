        <div class="_banner">
            <h2 class="bl-title">Improvements in Work from Home Routine</h2>
            <div class="_img">
                <img data-src="../assets/images/blog/explore-inner-img-3.png" alt="" class="img-responsive lozad">
            </div>
        </div>
        <div class="d-flex _content">
            <div class="col col-12 col-md-6 offset-md-2">
                <p>Well most of us are new to Working from home. We all could get mixed up in working and living out of the same place. It becomes hard for us not to get caught up in overdoing work and switching off at times and getting laid back in life.</p>
                <p>A lot of us feel differently towards working from home, some feel like they have more time on their hands or some feel the lack of productivity and creativity. </p>
                <p>Doing this for over a year now, we would suggest having a routine that could help you balance your life with work from home. Having a routine will help you do things that you keep procrastinating to do later, keep the mind fresh, get time to rejuvenate yourself. Read on for our tips on doing just that.</p>
                <h3>i) Move around</h3>
                <p>As working from home cuts down moving around for most of us, ensure to exercise in the morning or evening whatever suits your work schedule, try to walk around the house while doing your zoom calls and increase the number of steps you take everyday. Walk around the building to get some fresh air and a change of place.</p>
                <h3>ii) Use your Work Commute time wisely</h3>
                <p>If you were used to traveling to your workplace, now you can use that time to learn a new skill, read a book, quick run, prepare yourself for the day or cook your meals to save time during the day. Likewise use your evening commute time to do one of the above things instead of lazing around and also move your laptop equipment away from you to at least go on an evening stroll. </p>
                <h3>iii) Tidy up your space </h3>
                <p>We tend to keep the space messy as we live and work in the same place now. It’ll be great to set up a small work corner and spread your things in that space so that it does not feel like the entire room or flat is an office and not home.</p>
                <h3>iv) Plan your day</h3>
                <p>Plan your day in advance which will help you have a clear understanding what the day will look like, ensure to give yourself some breaks during the day to get enough rest and to do other things apart from work and also suggest your colleagues to take breaks during the day to reduce the workload and catch up with friends and family on whatsapp groups.</p>
                <h3>v) Stick to your Work Hours</h3>
                <p>It is very important to set your work timings because you tend to extend your work timings from home, but in general you don’t go back to the office to get work done after you log off. Doing this will give the brain some time off from the continuous mails and notifications.</p>
                <h3>vi) Don't spend too much time on Social Media</h3>
                <p>Most of the time we wonder where our productivity vanishes , check your screen time it will tell you exactly where you land up spending most of your time instead of doing the thing on your to do list. Set some time limit for your phone and leave it in the drawer to spend time with your thoughts and reflecting on your day.</p>
                <h3>vii) Shared Spaces / Shared Living</h3>
                <p>At, Boston Living, residents will have have no worries with respect to separating work life and Living as the the property is massive for the residents to work from hot desks, cafeteria’s, bookable meeting rooms, cosy common corners, private balconies, space to workout, futsal court, food to order in from the property itself, virtual events and whole community to interact and share ideas with.</p>

