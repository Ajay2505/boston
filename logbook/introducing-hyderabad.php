            <div class="col col-12 col-md-6 offset-md-2">
                <p>Hyderabad - also known as the city of the nizams and city of pearls, is located in South India. It is a city rich in tradition and history. Itâ€™s an alluring place having a blend of both the old and the new cultures. Tucked away in the historical past of the nation, you will find a plentitude of mosques and old bazaars, mystifying official plots and lavish malls to enthrall your experience. A veritable fusion of old culture voicing the history of India and the new city enrapturing a brighter future is what grabs the attention of visitors and propels one to see Hyderabad in all its glory.</p>
                <p><strong>Ethnicity of the People</strong> Referred to as "Hyderabadi", the residents of Hyderabad are predominantly Telugu and Urdu speaking people, with minority Bengali, Sindhi, Kannada, Memon, Nawayathi, Malayalam, Marathi, Gujarati, Marwari, Odia, Punjabi, Tamil and Uttar Pradeshi communities. Hyderabadi Muslims are a unique community who owe much of their history, language, cuisine, and culture to Hyderabad, and the various dynasties who previously ruled.</p>
                <p><strong>Religion</strong> Hindus are in the majority. Muslims form a very large minority, and are present throughout the city and predominate in and around old Hyderabad. There are also Christian, Sikh, Jain, Buddhist and Parsi communities and iconic churches, mosques and temples. </p>
                <p><strong>Languages Spoken</strong> Telugu and Urdu are both official languages of the city, and most Hyderabadis are bilingual. English is a "Secondary official language" used monumentally in business and administration, and it is an important medium of instruction in education and publications. A significant minority speak other languages, including Hindi, Bengali, Kannada, Marathi, Marwari, Odia and Tamil.</p>
                <p>Telangana's capital city is decked up with a plentitude of places to visit and you will be sure to have one of the most beautiful experiences while exploring Hyderabad, like the Charminar, Chowmahalla Palace, Golkonda Fort, etc. Hyderabad also prides itself for hosting several tourists every year, with the main attraction being the Taj Falaknuma Palace. However, Charminar symbolises the city. </p>
                <p>The Telugu film industry based in the city is the country's second-largest producer of motion pictures. Hyderabad also boasts of the Ramoji Film City, which is an integrated film studio. Several bollywood and tollywood movies have been shot here. The famous Baahubali has been shot here too, sets of which can be seen today. The studio allows tourists to visit the sets and has also created an amusement park within the studios. In 2005, Guinness Book of World Records called it the largest studio complex in the world.</p>
                <p>Hyderabad boasts highly of the food and cuisines it serves. The general cuisine served here is an amalgamation of Arabic, Turkish and Mughal cuisines coupled with some influence from the Telugu and Maratha cuisines. Hyderabad is most known for its Biryani, which is a dish cooked with multiple spices, basmati rice, browned onions and meats or vegetables of your choice. Through its cuisine, the city is listed as a UNESCO creative city of gastronomy.</p>
                <p>The warmth and facilities of this city makes it one of the most livable cities in India. Come experience this to believe it.</p>

